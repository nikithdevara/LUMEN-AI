// Helper function to map a resource title/string to a key in our content database.
// It normalizes strings to make matching robust against minor title discrepancies.
export function getResourceContent(title) {
  if (!title) return null;
  const t = title.toLowerCase();

  if (t.includes("signs") || t.includes("coercion") || t.includes("grooming")) {
    return resourceContents["online-coercion"];
  }
  if (t.includes("psychology") || t.includes("exploitation")) {
    return resourceContents["psychology-exploitation"];
  }
  if (t.includes("peer") || t.includes("vigilance")) {
    return resourceContents["peer-support"];
  }
  if (t.includes("hospitality") || t.includes("sop") || t.includes("hotel") || t.includes("workplace awareness framework")) {
    return resourceContents["hospitality-sop"];
  }
  if (t.includes("reporting") || t.includes("without risk")) {
    return resourceContents["reporting-risk"];
  }
  if (t.includes("digital") || t.includes("conversations") || t.includes("families") || t.includes("parent")) {
    return resourceContents["digital-conversations"];
  }
  if (t.includes("safe communities") || t.includes("communities guide") || t.includes("neighborhood")) {
    return resourceContents["safe-communities"];
  }
  if (t.includes("outreach") || t.includes("toolkit") || t.includes("shareable") || t.includes("graphics") || t.includes("material")) {
    return resourceContents["outreach-toolkit"];
  }
  if (t.includes("checklist") || t.includes("workplace awareness checklist")) {
    return resourceContents["workplace-checklist"];
  }

  // Fallback to online coercion as default
  return resourceContents["online-coercion"];
}
